for i=1:N
    ceq(1+(i-1)*2,:) = -[x(2*(N+1)+2*i+1); x(2*(N+1)+2*i+2)]' + [x(2*(N+1)+2*i-1); x(2*(N+1)+2*i)]' + h*g_func2114(k, d, eps, [x(2*(N+1)+2*i-1); x(2*(N+1)+2*i)]', [x(4*(N+1)+2*i-1); x(4*(N+1)+2*i)]');
    ceq(2+(i-1)*2,:) = -[x(4*(N+1)+2*i+1); x(4*(N+1)+2*i+2)]' + [x(4*(N+1)+2*i-1); x(4*(N+1)+2*i)]' + h*[x(2*i-1); x(2*i)]';
end

end

